class UserScopedModel {
  late int id;
  late String email, first_name, last_name, avatar;

  int getId() {
    return id;
  }

  String getEmail() {
    return email;
  }

  String getFirstName() {
    return first_name;
  }

  String getLastName() {
    return last_name;
  }

  String getAvatar() {
    return avatar;
  }
}
